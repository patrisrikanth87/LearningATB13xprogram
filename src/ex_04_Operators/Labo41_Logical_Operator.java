package ex_04_Operators;

public class Labo41_Logical_Operator {
    public static void main(String[] args) {
        boolean a = true;
        System.out.println(!a);  //false

        boolean b = true;
        System.out.println(!!b); //true

        boolean c = true || false;
        System.out.println(c);

    }
}
